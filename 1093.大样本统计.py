class Solution:
    def sampleStats(self, count: List[int]) -> List[float]:
        mi=0
        while mi<256 and count[mi]==0:
            mi+=1
        ma=255
        while ma>=0 and count[ma]==0:
            ma-=1

        s=0

        tot_cnt=0
        mode=-1
        most_cnt=0
        for i in range(mi,ma+1):
            cnt=count[i]
            if cnt==0:
                continue
            tot_cnt+=cnt
            s+=i*cnt
            if most_cnt<cnt:
                most_cnt=cnt
                mode=i

        mean=s/tot_cnt
        left,right=(tot_cnt+1)//2,(tot_cnt+2)//2
        median=0
        tot=0
        median=0
        for i in range(mi,ma+1):
            cnt=count[i]
            if cnt==0:
                continue
            _tot=cnt+tot
            if tot<left<=_tot:
                median+=i
            if tot<right<=_tot:
                median+=i
            tot=_tot
            if tot>=right:
                break
            
        return [mi,ma,mean,median/2,mode]


